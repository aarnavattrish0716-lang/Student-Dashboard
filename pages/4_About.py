import streamlit as st
st.title("🎓 About Student Performance Dashboard")
st.subheader("Brief introduction")
st.text("The Student Performance Dashboard is a multipage web application built using Streamlit.It allows users to manage student marks across different subjects and analyze their performance through interactive dashboards and visualizations.")
st.subheader("✨ Features")
st.markdown("""
- Add new student records
- Update marks for existing subjects
- Search student records
- Filter records by subject
- Sort records by marks
- Delete student records
- Subject-wise statistics
- Interactive charts
""")
st.subheader("🛠 Technologies")
st.write("""
- Python
- Streamlit
- Pandas
""")